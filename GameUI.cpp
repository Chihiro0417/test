GameUI::GameUI()
{
	m_pPlayerHp = new CPlayerHp;
	m_pPlayerHpSilhouette = new CPlayerHpSilhouette();
}

GameUI::~GameUI()
{
	
}

void GameUI::Draw()
{
	m_pPlayerHp->Draw();
	m_pPlayerHpSilhouette->Draw();

}

void GameUI::Update()
{
	m_pPlayerHp->Update();
}

